class SwarmConfig(BaseModel):
    """Runtime configuration for the swarm, loaded from environment variables."""

    nvidia_api_key: SecretStr = Field(
        ...,
        description="NVIDIA API key for DeepSeek model access.",
    )
    gemini_api_key: SecretStr = Field(
        ...,
        description="Google Gemini API key.",
    )
    gh_pat: SecretStr = Field(
        ...,
        description="GitHub Personal Access Token with repo scope.",
    )
    nvidia_base_url: str = Field(
        default="https://integrate.api.nvidia.com/v1",
        description="Base URL for the NVIDIA API.",
    )
    nvidia_model: str = Field(
        default="deepseek-ai/deepseek-coder-7b-instruct-v1.5",
        description="NVIDIA-hosted model identifier.",
    )
    gemini_model: str = Field(
        default="gemini-2.0-flash",
        description="Gemini model identifier.",
    )
    llama_model: str = Field(
        default="meta/llama-3.1-8b-instruct",
        description="Llama model identifier on NVIDIA.",
    )
    max_retries: int = Field(
        default=3,
        description="Maximum pipeline retry attempts.",
    )
    target_repo_owner: str = Field(
        default="CJRCODER",
        description="GitHub org or user that owns the target repo.",
    )

    @classmethod
    def from_env(cls) -> "SwarmConfig":
        """Construct a ``SwarmConfig`` from environment variables.

        Required env vars:
            - ``NVIDIA_API_KEY``
            - ``GEMINI_API_KEY``
            - ``GH_PAT``

        Raises:
            KeyError: If a required environment variable is missing.
        """

        def _require(name: str) -> str:
            value = os.environ.get(name)
            if not value:
                raise KeyError(
                    f"Required environment variable '{name}' is not set. "
                    f"Please export it before running the swarm."
                )
            return value

        return cls(
            nvidia_api_key=SecretStr(_require("NVIDIA_API_KEY")),
            gemini_api_key=SecretStr(_require("GEMINI_API_KEY")),
            gh_pat=SecretStr(_require("GH_PAT")),
        )
