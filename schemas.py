# Pipeline state
# ---------------------------------------------------------------------------

class ActionState(BaseModel):
    """Mutable state object that tracks the entire pipeline run."""

    pipeline_id: UUID = Field(
        default_factory=uuid4,
        description="Unique identifier for this pipeline execution.",
    )
    current_stage: PipelineStage = Field(
        default=PipelineStage.PLANNING,
        description="Current pipeline stage.",
    )
    feature_objective: str = Field(
        ...,
        description="The feature being built in this pipeline run.",
    )
    plan: Optional[ImplementationPlan] = Field(
        default=None,
        description="Plan produced by the Planner agent.",
    )
    code: Optional[CodePayload] = Field(
        default=None,
        description="Code produced by the Implementer agent.",
    )
    qa_report: Optional[QAReport] = Field(
        default=None,
        description="QA report produced by the QA Reviewer agent.",
    )
    git_result: Optional[GitPushResult] = Field(
        default=None,
        description="Result of the Git push operation.",
    )
    retries: int = Field(
        default=0,
        le=3,
        description="Number of retry attempts (max 3).",
    )
    started_at: datetime = Field(
        default_factory=lambda: datetime.now(timezone.utc),
        description="UTC timestamp when the pipeline started.",
    )
    completed_at: Optional[datetime] = Field(
        default=None,
        description="UTC timestamp when the pipeline finished.",
    )
    error_log: list[str] = Field(
        default_factory=list,
        description="Chronological log of errors encountered.",
    )

    # -- helpers -------------------------------------------------------------

    def is_terminal(self) -> bool:
        """Return ``True`` if the pipeline has reached a terminal state."""
        return self.current_stage == PipelineStage.DONE or self.retries > 3

    def mark_complete(self) -> None:
        """Transition the pipeline to the DONE stage."""
        self.current_stage = PipelineStage.DONE
        self.completed_at = datetime.now(timezone.utc)

    def log_error(self, msg: str) -> None:
        """Append a timestamped error message to the error log."""
        timestamp = datetime.now(timezone.utc).isoformat()
        self.error_log.append(f"[{timestamp}] {msg}")


